package sample;

public class Deputy {
    public int ID_Deputy;
    public String FIO;
    public String Addres;
    public String Phone_O;
    public String Phone_H;
    public String Sex;

    public Deputy(int ID_Deputy, String FIO, String Addres, String Phone_O, String Phone_H, String Sex) {
        this.ID_Deputy = ID_Deputy;
        this.FIO = FIO;
        this.Addres = Addres;
        this.Phone_O = Phone_O;
        this.Phone_H = Phone_H;
        this.Sex = Sex;
    }
}
